#STEAM AND LEAF DIAGRAMS

pulse=c(89,68,92,74,76,65,77,83,75,87,85,64,79,77,96,80,70,85,80,80,
        82,81,86,71,90,87,71,72,62,78,77,90,83,81,73,80,78,81,81,75,
        82,88,79,79,94,82,66,78,74,72)
stem(pulse)
